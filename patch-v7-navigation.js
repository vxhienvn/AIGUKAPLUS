source = source.replaceAll("⚙ Quản lý Page/TEST", "⚙ Điều khiển BOT & Lịch");
source = source.replaceAll("AI Học có kiểm duyệt", "AI Học & Quản lý Prompt");
const learningNav = "${nav('/v8-learning','AI Học & Quản lý Prompt','learning')}";
